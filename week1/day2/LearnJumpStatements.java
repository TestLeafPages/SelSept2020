package week1.day2;

public class LearnJumpStatements {

	public static void main(String[] args) {
		
		
		
		for (int i = 1; i <= 5; i++) {
			
			if (i == 3) {
				System.out.println("Three");
				
				//break;
				continue;
			}
			
			System.out.println(i);
			
		}
		
		

	}

}
